"""
題目：3
請寫一個 Python 函式: get_index(scores, subject_no)
可以在不改變原來成績資料(scores)的順序下，針對指定的欄位(subject_no)進行索引排序，並傳回記錄排序順序的索引串列。
而該索引值(資料的第幾項)串列記錄內容: 該指定欄位由小到大的排序結果。
請引用上述函式，把總分的部分進行由小到大的排序，並把排序的結果附加到每筆資料的最後面。
例如，假設有一個資料組合如下:(每列最後一項是該列的倒數分數排名，即由小到大。)
學生姓名	國文	英文	數學	總分
駱俊翔	 72	 84	 85	241	 11	
陳佳玲	 43	 13	 33	 89	  2	
鄭志丹	 77	 68	  4	149	  6	
鄭淑玲	 90	 53	 42	185	  9	
藍婉菁	 76	 81	 56	213	 10	
李仰夫	 49	  6	 91	146	  5	
王美華	 39	  4	 36	 79	  1	
霍秀蓉	 62	 41	 62	165	  8	
陳建佑	 64	 19	 55	138	  4	
林俊賢	 16	 80	 39	135	  3	
馮惠雯	 83	 38	 39	160	  7
"""
def get_index(scores, subject_no):
    # 複製一份原始資料的索引
    original_index = list(range(len(scores)))

    # 依指定欄位進行排序，並取得排序後的索引
    sorted_index = sorted(original_index, key=lambda i: scores[i][subject_no])

    return sorted_index

# 原始成績資料
scores = [
    ["駱俊翔", 35, 29, 72, 136, 0],
    ["陳佳玲", 97, 0, 91, 188, 0],
    ["鄭志丹", 17, 71, 60, 148, 0],
    ["鄭淑玲", 57, 18, 75, 150, 0],
    ["藍婉菁", 52, 70, 50, 172, 0],
    ["李仰夫", 48, 85, 93, 226, 0],
    ["王美華", 50, 99, 29, 178, 0],
    ["霍秀蓉", 14, 45, 91, 150, 0],
    ["陳建佑", 51, 38, 93, 182, 0],
    ["林俊賢", 33, 14, 49, 96, 0],
    ["馮惠雯", 16, 62, 68, 146, 0],
]

# 對總分進行排序
total_score_index = get_index(scores, 4)

# 更新原始資料的倒數分數排名
for i, index in enumerate(total_score_index, start=1):
    scores[index][-1] = i

# 列印結果
print(f"學生姓名 國文\t英文\t數學\t總分")
for student in scores:
    print('\t'.join(map(str, student)))
